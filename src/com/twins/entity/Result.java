package com.twins.entity;

public class Result {
	private String fileA;
	private String fileB;
	private float score;
	private int sameLines;
	public String getFileA() {
		return fileA;
	}
	public void setFileA(String fileA) {
		this.fileA = fileA;
	}
	public String getFileB() {
		return fileB;
	}
	public void setFileB(String fileB) {
		this.fileB = fileB;
	}

	public float getScore() {
		return score;
	}
	public void setScore(float score) {
		this.score = score;
	}
	public int getSameLines() {
		return sameLines;
	}
	public void setSameLines(int sameLines) {
		this.sameLines = sameLines;
	}
	
	
}
